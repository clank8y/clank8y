export function buildReviewPrompt(promptContext?: string): string {
  const normalizedPromptContext = promptContext?.trim()
  if (!normalizedPromptContext) {
    return BASE_REVIEW_PROMPT
  }

  return [
    BASE_REVIEW_PROMPT,
    '',
    normalizedPromptContext,
  ].join('\n')
}
