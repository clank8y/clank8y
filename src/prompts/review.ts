export function buildReviewPrompt(promptContext: string): string {
  const normalized = promptContext.trim()
  if (!normalized) {
    return BASE_REVIEW_PROMPT
  }

  return [
    BASE_REVIEW_PROMPT,
    '',
    normalized,
  ].join('\n')
}
