const BASE_INCIDENT_FIX_PROMPT = [
  PERSONA,
  '',
  KNOWLEDGE_VERIFICATION,
  '',
  INCIDENT_FIX_MISSION,
  '',
  INCIDENT_FIX_SCOPE,
  '',
  INCIDENT_FIX_WORKFLOW,
  '',
  INCIDENT_FIX_REPORT_GUIDANCE,
].join('\n')

export function buildIncidentFixPrompt(promptContext: string): string {
  const normalized = promptContext.trim()
  if (!normalized) {
    return BASE_INCIDENT_FIX_PROMPT
  }

  return [
    BASE_INCIDENT_FIX_PROMPT,
    '',
    'Here is the incident context for this run:',
    normalized,
  ].join('\n')
}
